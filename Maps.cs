        public void Draw(SpriteBatch spriteBatch)
        {
            if (Keyboard.GetState().IsKeyDown(Keys.M))
            {
                PickMap();
            }
            for(int y = 0; y < map1.GetLength(0); y++)
            {
                for (int x = 0; x < map1.GetLength(1); x++)
                {
                    if (map1[y,x] == 1)
                    {
                        Tile.X = TileSize * x;
                        Tile.Y = TileSize * y;
                        Tile.Width = TileSize;
                        Tile.Height = TileSize;
                        spriteBatch.Draw(texture, Tile, Color.DarkGreen);
                    }
                    if (map1[y,x] == 2)
                    {   
                        Enemy = new Enemy(texture, new Vector2(TileSize * x + 15, TileSize * y + 30), this);
                        Enemies.Add(Enemy);
                        map1[y,x] = 0;
                    }
                    if (map1[y,x] == 3)
                    {
                        Tile.X = TileSize * x;
                        Tile.Y = TileSize * y;
                        Tile.Width = TileSize;
                        Tile.Height = TileSize;
                        foreach (Attack attack in game.Player1.ActiveAttacks)
                        {
                            if (attack.attack.Intersects(Tile))
                            {
                                map1[y,x] = 0;
                            }
                        }
                        if (game.Player1.GroundPounding == true && game.Player1.player.Intersects(Tile))
                        {
                            map1[y,x] = 0;
                        }
                        spriteBatch.Draw(texture, Tile, Color.SandyBrown);
                    }

                    //4 is transparent

                    if(map1[y,x] == 5)
                    {
                        Tile.X = TileSize * x;
                        Tile.Y = TileSize * y;
                        Tile.Width = TileSize;
                        Tile.Height = TileSize;
                        if (Tile.Intersects(game.Player1.player))
                        {
                            game.Player1.HP = -1;
                        }
                        foreach (Enemy enemy in Enemies)
                        {
                            if (Tile.Intersects(enemy.enemy))
                            {
                                enemy.HP = -1;
                            }
                        }
                        spriteBatch.Draw(spikes, Tile, Color.DarkGray);
                    }
                }
            }
            for (int i = 0; i < Enemies.Count(); i++)
            {
               if (Enemies[i].HP <= 0)
                {
                    DeadEnemies.Add(Enemies[i]);    
                }                    
                if (Enemies[i].HP > 0)
                {
                    Enemies[i].Draw(spriteBatch);
                }
            }
            foreach (Enemy dead in DeadEnemies)
            {
                Enemies.Remove(dead);
            }
            if (DeadEnemies.Count > 1)
            {
                DeadEnemies.RemoveAt(0);
            }
        }

        public void Update()
        {
            foreach (Enemy enemy in Enemies)
            {
                enemy.Update(game.Player1);
            }
        }

        public void PickMap()
        {
            Random rnd = new Random();
            map1 = AllMaps[rnd.Next(0, AllMaps.Count())];
        }
    }
