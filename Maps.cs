                for (int x = 0; x < map1.GetLength(1); x++)
                {
                    if (map1[y,x] == 1)
                    {
                        Tile.X = TileSize * x;
                        Tile.Y = TileSize * y;
                        Tile.Width = TileSize;
                        Tile.Height = TileSize;
                        spriteBatch.Draw(texture, Tile, Color.DarkGreen);
                    }
                    if (map1[y,x] == 2)
                    {   
                        Enemy = new Enemy(texture, new Vector2(TileSize * x + 15, TileSize * y + 30));
